import random


# [마법의 보자기 함수] 
# *value 라고 쓰면, 배달되는 숫자가 3개든 100개든 `value`라는 하나의 튜플 묶음으로 다 받아냅니다!
def sum_all(*value):
    sum = 0
    # 보자기에 싸인 알맹이들의 진짜 종류를 찍어봅니다. (결과는 <class 'tuple'> 이에요!)
    print(type(value))
    
    # 튜플 안에 들어있는 숫자들을 하나씩 꺼내서 `sum`에 계속 더해줍니다. (합계 구하기)
    for i in value:
        sum += i
        
    # len(value)는 보자기에 들어있는 숫자가 총 몇 개인지 개수를 세어줘요!
    # 전체 합계를 개수로 나누어서 '평균(avr)'을 계산합니다.
    avr = sum / len(value)
    
    # 합계와 평균을 튜플 형태로 한꺼번에 메인 함수로 돌려줍니다.
    return sum, avr


def main():
    # --- [실험 1: 숫자를 내 마음대로 여러 개 던지기] ---
    # 배달할 숫자를 7개나 던졌어요! 
    # sum_all 함수는 `*value` 보자기 덕분에 에러 없이 이 7개를 튜플 하나로 싹 묶어서 처리합니다.
    s, a = sum_all(100, 200, 300, 500, 123, 5234, 314)
    print(f"합계는 {s}, 평균은 {a}")

    print("-" * 40)

    # --- [실험 2: 리스트를 풀어서 던지기] ---
    # 0~100 사이의 랜덤 숫자 100개가 들어있는 리스트를 만듭니다.
    list_a = [random.randint(0, 100) for _ in range(100)]
    
    # [여기 중요! ⭐] `sum_all(*list_a)`에서 리스트 앞에 붙은 별(`*`)은 
    # "리스트의 대괄호 `[]`를 가위로 싹둑 잘라서 안에 든 숫자 100개를 낱개로 펼쳐서 던져라!" 라는 뜻이에요.
    # 그러면 함수는 그 100개의 숫자를 또 `*value` 보자기로 이쁘게 받아서 합계와 평균을 내줍니다.
    s, a = sum_all(*list_a)
    print(f"합계는 {s}, 평균은 {a}")


if __name__ == "__main__":
    main()