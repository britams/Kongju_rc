import math

class MinusError(Exception):
    def __init__(self):
        message = f"음수는 허용되지 않습니다."
        super().__init__(message)
        #super 부모클래스 불러오는 것.
        # =========================================================================
# 📌 super().__init__(message) 완벽 핵심 요약
# =========================================================================
#
# 1. [단어 뜻]
#    - super()  : 나를 만들어준 "부모 클래스(상위 클래스)"를 가리킵니다.
#    - .__init__: 클래스의 객체가 만들어질 때 가장 먼저 실행되는 "초기화(기초 공사) 함수"입니다.
#    - (message): 에러 화면에 띄워줄 "경고 메시지 글자"입니다.
#
# 2. [한 줄 요약]
#    - "부모 클래스형아! 내가 넘겨받은 에러 메시지(message)를 줄 테니까,
#       부모님이 원래 하던 대로 에러 세팅(초기화) 좀 대신 먼저 해줘!"라는 뜻입니다.


def main():
    user_input = input("정수 입력: ")
    try:
        number_input = int(user_input)
     # except Exception as e:
    except MinusError as e:
        print(e)
    except ValueError as e:
        print(e)
    else:
        print(f"원의 반지름: {number_input}")
        print(f"원의 둘레: {number_input * 2 * math.pi}")
        print(f"원의 넓이: {math.pi * number_input**2}")
    finally:
        print("----------프로그램이 정상 종료되었습니다.")


if __name__ == "__main__":
    main()
#robust 한 코드를 만드는 방법 일부로 오류를 만든다. error 받아서 처리. 
# =========================================================================
# 🛠️ ROBUST(견고한) 코드를 만드는 3단계 비유와 원리
# =========================================================================
#
# [1단계] 🥊 일부러 오류를 만든다 (스트레스 테스트)
# -------------------------------------------------------------------------
# - 완벽해 보이는 자동차를 만들었어도, 일부러 벽에 들이받는 '충돌 테스트'를 하죠?
# - 코드도 똑같아요. 정상적인 값만 넣는 게 아니라 일부러 이상한 값을 마구 던져봅니다.
#   👉 예: 나이를 입력하라고 만든 칸에 일부러 "삼십세"라는 글자를 넣어보거나,
#   👉 인터넷 연결을 일부러 끊어버리거나, 존재하지 않는 파일을 열어보라고 시키는 것!
#
# [2단계] 🕸️ Error를 받아서 처리한다 (에러 그물망 치기)
# -------------------------------------------------------------------------
# - 그렇게 일부러 충돌을 일으키면 프로그램이 어디서, 왜 터지는지(Error) 알게 됩니다.
# - 그럼 그 터지는 길목마다 `try-except` 같은 그물망을 쳐서 안전장치를 만듭니다.
#   👉 "어? 사용자가 글자를 입력하니까 에러가 나네? 
#       그럼 글자가 들어오면 '숫자만 입력해주세요'라고 친절하게 안내하고 
#       다시 입력받게 만들자!" 하고 처리(Handle)하는 것이죠.
#
# [3단계] 💪 결과: Robust(견고한) 코드 완성!
# -------------------------------------------------------------------------
# - 이 과정을 무한 반복하면, 사용자가 아무리 이상한 짓을 해도, 
#   서버가 잠시 맛이 가도 프로그램이 강제 종료되지 않고 유연하게 대처합니다.
# - 즉, 맷집이 어마어마하게 강해진 "견고한 코드"가 탄생하는 것이죠!

