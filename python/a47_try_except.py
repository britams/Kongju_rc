# 1. [정의] 
#    - try-except 구문과 함께 쓰이며, 앞의 코드가 성공하든 에러가 나든 
#      "무조건! 마지막에! 꼭! 실행"되도록 보장하는 절대적인 마무리 영역입니다.
#
# 2. [비유] 
#    - 영화 촬영장에서 날씨가 좋아서 촬영에 대성공을 거두었든(정상 실행),
#    - 갑자기 폭우가 쏟아져서 촬영이 중간에 멈췄든(에러 발생),
#    - "빌린 카메라 장비 반납 및 세트장 청소"는 무조건 하고 가야 하는 것과 같습니다.
#
# 3. [왜 쓸까?]
#    - 함수 중간에 return(종료)을 만나서 프로그램이 도망치려고 해도,
#    - 파이썬이 바짓가랑이를 붙잡고 "잠깐! 나가더라도 finally는 실행해!" 하고
#      강제로 실행시킵니다. (주로 파일 닫기, DB 연결 끊기 등 메모리 청소에 필수!)
