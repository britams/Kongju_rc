# [임포트] 파이썬 내장 도구함에서 제공하는 '자동 캐시(LRU)' 데코레이터를 가져옵니다.
from functools import lru_cache


def rec_fac(n):
    # [일반 재귀 팩토리얼] 
    # - 자기 자신을 계속 호출하는 전통적인 재귀 방식이에요.
    # - 파이썬은 재귀를 너무 많이 돌면 에러(RecursionError)가 나서 추천하지 않는 방식입니다!
    if n == 1:
        return 1
    else:
        return n * rec_fac(n-1)


def for_fac(n):
    # [반복문 팩토리얼]
    # - 팩토리얼을 구할 때 가장 정석적이고 안전한 방식이에요.
    # - 메모리를 낭비하지 않고 속도도 안정적이라 재귀보다 훨씬 추천됩니다!
    output = 1
    for i in range(n):
        output *= i+1
    return output


# -------------------------------------------------------------------------
# 💾 [수동 캐싱(Memoization) 테스트용 전역 변수 세팅]
# -------------------------------------------------------------------------
cnt = 0        # fibonacci_memory 함수가 실제로 문을 열고 들어온 횟수 카운트
dic_cnt = 0    # 새로운 연산을 수행해서 딕셔너리에 '저장'한 횟수 카운트
dictionary = {1: 1, 2: 1} # 1번째와 2번째 피보나치 숫자를 미리 넣어둔 수동 창고!


def fibonacci_memory(n):
    global cnt, dic_cnt
    cnt += 1  # 함수가 호출될 때마다 무조건 1씩 증가!
    
    # [수동 캐시 체크] 딕셔너리 창고에 이미 계산된 값이 있다면? 다시 계산 안 하고 즉시 반환!
    if n in dictionary:
        return dictionary[n]
    else:
        # 창고에 없는 처음 보는 숫자라면 아래의 재귀 연산을 수행합니다.
        output = fibonacci_memory(n-1) + fibonacci_memory(n-2)
        
        # ★중요: 다음 번에 또 귀찮게 연산 안 하도록 결과를 창고에 딱 박아둡니다! (캐싱)
        dictionary[n] = output
        dic_cnt += 1 # 창고에 새로 집어넣은 횟수 추가!
        return output


# -------------------------------------------------------------------------
# 🔥 [파이썬 치트키: @lru_cache를 이용한 자동 캐싱]
# -------------------------------------------------------------------------
# - maxsize=None : "메모리 한도 없이 무제한으로 다 기억해 둬!"라는 뜻이에요.
# - 우리가 위에서 수동으로 만든 dictionary 작업을 파이썬 보디가드가 알아서 다 해줍니다.
# - 한 번 계산한 n이 또 들어오면 함수를 실행하지도 않고 값을 즉시 돌려줘요!
# -------------------------------------------------------------------------
@lru_cache(maxsize=None)
def fibonacci(n):
    if n == 1:
        return 1
    if n == 2:
        return 1
    else:
        # 이 재귀 호출들도 @lru_cache가 중간에서 가로채기 때문에 
        # n이 400이어도 렉 걸리지 않고 빛의 속도로 계산이 끝납니다!
        return fibonacci(n-1) + fibonacci(n-2)


def main():
    print("--- 1. 팩토리얼 비교 실험 ---")
    print(f"재귀 팩토리얼(100) 결과: {rec_fac(100)}")
    print(f"for문 팩토리얼(100) 결과: {for_fac(100)}")
    
    print("\n" + "="*40 + "\n--- 2. 자동 캐시(@lru_cache) 실험 ---")
    # 캐시가 없다면 400번 피보나치는 전 우주가 멸망할 때까지 컴퓨터가 계산해야 해요.
    # 하지만 자동 캐시 덕분에 눈 깜짝할 사이에 결과가 나옵니다!
    print(f"자동 캐시 피보나치(400) 결과값: {fibonacci(400)}")
    
    print("\n" + "="*40 + "\n--- 3. 통계 데이터 분석 ---")
    # 메인 함수에서 fibonacci_memory를 따로 호출하지 않았기 때문에 0, 0이 나오는 게 맞아요!
    # 만약 위에서 fibonacci_memory(25)를 돌렸다면 호출 횟수가 엄청나게 절약된 걸 볼 수 있답니다.
    print(f"fibonacci_memory 함수가 실제로 실행된 총 횟수: {cnt}번")
    print(f"새롭게 계산해서 수동 딕셔너리 창고에 저장한 횟수: {dic_cnt}번")


if __name__ == "__main__":
    main()