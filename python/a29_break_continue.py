import random  # 'random'이라는 파이썬 도구 상자를 가져옵니다.


def main():
    # 0부터 100 사이의 랜덤 숫자를 100번 뽑아서 리스트 `li` 안에 쏙쏙 채웁니다.
    li = [random.randint(0, 100) for _ in range(100)]  # _ 언더바 하나는 변수를 설정하지 않는다는 의미.

    # 100개의 랜덤 숫자가 들어있는 리스트를 먼저 보여줍니다.
    print("원본 리스트:", li)
    print("-" * 40)  # 구분을 위한 줄 긋기!

    # 리스트 `li`에 들어있는 숫자들을 앞에서부터 하나씩 `number`에 넣으며 반복합니다.
    for number in li:
        
        # [조건 1] 만약 숫자가 50보다 작다면?
        if number < 50:
            continue  # 아래의 print나 break는 구경도 하지 말고, 다음 숫자로 바로 패스(점프)!

        # 50 이상인 숫자들만 이 선을 넘어와서 화면에 출력됩니다.
        # end=" "로 수정해서 쉼표 대신 깔끔하게 띄어쓰기로 숫자를 구분해 줄게요!
        print(number, end=" ")

        # [조건 2] 만약 숫자가 80보다 크다면?
        if number > 80:
            break  # "여기까지!"를 외치며 for 반복문을 그 즉시 완전히 부수고 탈출합니다!

    print("\n[안내] 80보다 큰 숫자가 나와서 반복을 종료했습니다!")


if __name__ == "__main__":
    main()