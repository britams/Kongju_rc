import time  # 타임은 모듈! 도구 상자.


def main():
    # 1. 사람이 읽기 편한 날짜와 시간으로 보여줍니다.
    # 출력 예시: 'Wed Jun 24 09:47:34 2026' (요일 월 일 시:분:초 년도)
    print(time.asctime())

    # 2. 컴퓨터가 계산하기 편한 '타임스탬프(Timestamp)'를 초 단위로 보여줍니다.
    # 1970년 1월 1일 0시 0분 0초부터 지금까지 흘러온 전체 초(second)를 뜻해요.
    # 소수점 아래는 밀리초(ms), 마이크로초(μs) 같은 아주 짧은 시간이에요!
    print(time.time())

    # 3. 컴퓨터 내부의 특정 시계(여기서는 1번 시계)를 기준으로 흐른 시간을
    # '나노초(ns, 10억 분의 1초)'라는 엄청나게 정밀한 단위로 보여줍니다.
    # 프로그램이 실행되는 데 걸리는 아주 미세한 시간을 잴 때 주로 사용해요.
    print(time.clock_gettime_ns(1))

    # --- 여기서부터 우리 코린이가 작성한 멋진 코드! ---

    # ptime(Previous Time, 이전 시간)이라는 변수에 '현재 시간(초)'을 똑같이 저장해 둡니다.
    # 예: 지금이 딱 100초라면 ptime에는 100이 들어갑니다.
    ptime = time.time()

    # 숫자를 세기 위한 바구니(cnt)를 만들고 0을 넣어줍니다.
    cnt = int()

    # "현재 시간이 (처음 시작한 시간 + 5초)보다 작은 동안에는" 계속 반복해라!
    # 예: 100초에 시작했다면, 현재 시간이 105초가 되기 전까지 계속 이 안에서 뱅글뱅글 돕니다.
    while time.time() < ptime + 5:
        # 루프가 한 바퀴 돌 때마다 카운트(cnt)를 1씩 더해줍니다.
        # 컴퓨터가 노는 시간(sleep) 없이 풀파워로 돌기 때문에 엄청나게 빠른 속도로 숫자가 올라가요!
        cnt += 1

    # 5초가 지나서 while 루프를 탈출하면, 그동안 쌓인 숫자를 멋지게 출력합니다.
    print(f"이 컴퓨터는 5초동안 {cnt} 카운트가 진행되었습니다.")


if __name__ == "__main__":
    main()