# 1. 'time'이라는 파이썬 도구 상자를 가져옵니다. (시간을 멈추는 기능을 쓰기 위해 필요해요!)
import time


# main이라는 이름의 함수(기능 덩어리)를 정의합니다.
def main():
    # int()는 정수 0을 만들어냅니다. 즉, i라는 변수에 0을 처음으로 넣어준 거예요.
    i = int()

    # i가 10보다 작은 동안에는 아래에 있는 코드를 계속 반복합니다.
    while i < 10:
        # 화면에 "X 번째 실행중"을 출력해요. 
        # i는 0부터 시작하니까 보기 편하게 1을 더해서 1번째, 2번째...로 보이게 합니다.
        print(f"{i + 1} 번째 실행중")

        # i에 1을 더해줍니다. (이게 없으면 i가 영원히 0이라서 무한 루프에 빠져요!)
        i += 1

    # 예외 처리 시작! "일단 이 코드를 시도(try)해봐!"라는 뜻입니다.
    try:
        # 조건이 'True'(참)이므로, 무조건 무한히 반복하는 루프를 만듭니다.
        while True:
            # 화면에 점('.')을 찍습니다. end=""는 줄바꿈을 하지 말고 옆으로 이어 붙이라는 뜻이에요.
            print(".", end="")

            # 0.1초 동안 컴퓨터를 잠깐 재웁니다(대기합니다). 
            # FPS(초당 프레임) 계산만큼 컴퓨터를 '블로킹(잠깐 멈춤)'해서 속도를 조절하는 원리예요!
            time.sleep(0.1)  

    # 만약 사용자가 키보드로 강제 종료(Ctrl + C)를 누르면 이쪽(except)으로 탈출합니다.
    except KeyboardInterrupt:
        # \n은 줄바꿈(엔터)을 의미해요. 점들이 찍히던 줄 다음 줄에 깔끔하게 글자를 출력합니다.
        print("\n키보드 인터럽트!!")
        
    # --- 여기서부터 새로 추가된 보너스 스테이지! ---
    
    # list("문자열")을 하면 글자들을 한 글자씩 쪼개서 멋진 '글자 리스트'로 만들어줘요.
    # 공백(띄어쓰기)도 하나의 글자로 취급해서 리스트에 쏙 들어갑니다!
    list_test = list("seohyunlee python learner")
    
    # 글자들이 잘 쪼개져서 리스트가 되었는지 화면에 한 번 보여줍니다.
    print(list_test)
    print(type(list_test))
    
    # "글자 리스트 안에 알파벳 's'가 들어있는 동안에는" 아래 코드를 계속 반복해라!
    while "s" in list_test:
        # 리스트 안에서 가장 먼저 발견되는 's'를 찾아서 슥삭 지워버립니다(remove).
        # 's'가 여러 개 있으면, 이 while 루프가 돌면서 하나씩 차례대로 다 지워줄 거예요.
        list_test.remove("s")
        
    # 's'가 전부 다 지워지고 매끈해진 리스트를 최종적으로 화면에 출력합니다!
    print(list_test)

 
# 이 파일이 직접 실행되었을 때만 main() 함수를 작동시키라는 파이썬의 규칙입니다.
if __name__ == "__main__":
    main()