# =========================================================================
# [정의부] 함수를 만드는 곳이에요.
#방 종류 총정리:
# 1. value : 이름 없는 일반 배달을 무조건 받아야 하는 기본 방
# 2. n=2, v=3, g=5 : 값을 안 주면 기본 세팅값으로 채워지는 '디폴트 파라미터' 방
# 3. *sum : 앞 방들이 다 차고 남은 '이름 없는 찌꺼기 숫자'들을 다 쓸어 담는 마법의 보자기 방
# =========================================================================
def print_n_times(value, n=2, v=3, g=5, *sum):
    sum_ = 0  # 보자기 안에 든 숫자들을 더해서 누적할 바구니예요.
    
    # [1단계] n 방에 들어온 숫자만큼 value를 반복해서 출력해요.
    for i in range(n):
        print(value)
        
    # [2단계] 만약 *sum 보자기 방에 찌꺼기 숫자가 들어왔다면, 하나씩 꺼내서 sum_에 더해줍니다.
    for i in sum:
        sum_ += i
        
    # [3단계] 열심히 더한 합계(sum_)를 자신을 호출한 곳으로 돌려줍니다(return).
    return sum_


# =========================================================================
# [실행부] 함수를 가져다 쓰는 메인 센터예요.
# =========================================================================
def main():
    # positional, default, 가변인자
    # 가변인자가 없으면, default 인자 or keyword 인자. 
    print(print_n_times("안녕하세요", n=10, g=123, v=123))
    # ---------------------------------------------------------------------
    # 메인 실험 0: 강사님이 말씀하신 '디폴트 파라미터 임의 지정(키워드 인자)' 기법!
    # ---------------------------------------------------------------------
    print("--- 임의 지정 실험 시작 ---")
    
    # 배달 분석:
    # - "안녕하세요": 이름표가 없으므로 무조건 맨 첫 번째 방인 [value]로 쏙 들어갑니다. (위치 인자)
    # - g=10, n=2, v=54123: 이름표를 붙여서 던졌기 때문에, 순서가 원래 정의된 것과 달라도
    #   파이썬이 찰떡같이 [g 방], [n 방], [v 방]을 찾아서 값을 넣어줍니다! (임의 지정/키워드 인자)
    # - *sum 보자기: 이름표 배달이 시작된 이후로는 이름 없는 찌꺼기를 던질 수 없어서 자동으로 텅 빕니다.
    # 결과: n이 2이므로 "안녕하세요"가 2번 출력되고, 보자기가 비어있어 함수는 0을 돌려줍니다.
    result = print_n_times("안녕하세요", g=10, n=2, v=54123)
    print(f"함수가 돌려준 값: {result}")

    print("\n" + "="*40 + "\n")

    # ---------------------------------------------------------------------
    # 기존 실험 1: 순서대로 배달하기 (기본값 방까지만 채우기)
    # ---------------------------------------------------------------------
    print("--- 실험 1 시작 ---")
    
    # 배달 분석:
    # - "안녕하세요": 이름표가 없으니 순서대로 1등 방인 [value]로 배달됩니다.
    # - 10: 이름표가 없으니 순서대로 그다음 방인 [n]으로 배달됩니다! (기본값 2는 무시되고 10이 됨)
    # - 뒤에 남은 v, g, *sum 방에는 줄 값이 없으니 v=3, g=5가 채워지고 *sum 보자기는 텅 빕니다.
    # 결과: n이 10이 되었으므로 "안녕하세요"가 10번 출력되고, 리턴값은 0이 됩니다.
    result1 = print_n_times("안녕하세요", 10)
    print(f"실험 1의 보자기 합계: {result1}")

    print("\n" + "="*40 + "\n")

    # ---------------------------------------------------------------------
    # 기존 실험 2: 순서대로 배달해서 마법의 보자기(*sum)까지 꽉 채우기
    # ---------------------------------------------------------------------
    print("--- 실험 2 시작 ---")
    
    # 배달 분석 (이름표가 없는 일반 배달만 쭉 나열했어요!):
    # - "반가워" -> 1등 방 [value]로 배달
    # - 2       -> 2등 방 [n]으로 배달 (n=2)
    # - 3       -> 3등 방 [v]으로 배달 (v=3)
    # - 5       -> 4등 방 [g]으로 배달 (g=5)
    # - 그러고도 남은 진짜 찌꺼기 숫자들인 (100, 200, 300) -> 드디어 [*sum 보자기]로 한꺼번에 쏙!
    # 결과: n이 2이므로 "반가워"가 2번 출력되고, 보자기 안의 100+200+300이 더해져서 600이 리턴됩니다.
    result2 = print_n_times("반가워", 2, 3, 5, 100, 200, 300)
    print(f"실험 2의 보자기 합계 (100+200+300): {result2}")


if __name__ == "__main__":
    main()