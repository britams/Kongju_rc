# a: int ➡️ "a 자리에는 정수(숫자)를 주세요!"
# value: str ➡️ "원래 힌트는 문자열(str)이지만, 아래 코드에서 숫자인지 검사할 거예요!"
def print_hello(a: int, value: str):
    """이 함수는 수업에서 작성한 예시 함수 (독스트링 설명서)

    Args:
         a (int): 반복하는 횟수
         value (str): 추가 프린트할 데이터 (실제 숫자가 들어오면 한 번 더 반복해요!)

    Returns:
        tuple: 성공여부, 숫자, 소수를 한꺼번에 묶은 튜플을 돌려줘요!
    """

    # 1. 전달받은 숫자 'a'만큼 반복합니다. (메인에서 3을 보냈으니 3번 반복해요!)
    for i in range(a):
        # 화면에 "안녕하세요!"와 넘겨받은 value, 그리고 현재 몇 번째 반복 중인지(i)를 출력합니다.
        print("안녕하세요!", value, i)

    # 2. [isinstance 검사기] python에서 데이터 종류(type)를 체크할 때 사용해요!
    if isinstance(value, int): 
        # `value`에 들어있는 숫자만큼 또 반복합니다.
        for _ in range(value):
            # 화면에 "test"를 출력합니다.
            print("test")

    # [여기 핵심! ⭐] 파이썬에서는 return 뒤에 쉼표(,)를 써서 여러 값을 보낼 수 있어요!
    # 겉보기엔 3개 같지만, 사실 파이썬이 ("execition OK!", 123, 3.14) 라는 '하나의 튜플'로 예쁘게 묶어서 던져줍니다!
    return "execition OK!", 123, 3.14 


def main():
    # -------------------------------------------------------------
    # [개념 1: 튜플 언패킹 (쪼개서 받기)]
    # 함수가 던져준 튜플 ("execition OK!", 123, 3.14)을 3개의 방에 나눠 담습니다!
    # re = "execition OK!", rel = 123, 그리고 마지막 3.14는 언더바(_) 방에 넣어서 버립니다!
    # -------------------------------------------------------------
    re, rel, _ = print_hello(3, 2) 
    print("쪼개서 받은 결과:", re, rel)
    
    print("-" * 30)

    # -------------------------------------------------------------
    # [개념 2: 스타(*) 연산자 언패킹]
    # 함수가 준 튜플을 통째로 `re`에 담으면, `re`는 튜플 세트가 돼요.
    # 이때 `print(*re)`처럼 앞에 별(`*`)을 붙여주면 튜플의 괄호를 싹 벗겨서 알맹이만 옆으로 펼쳐서 출력해 줍니다!
    # -------------------------------------------------------------
    re = print_hello(3, 2)
    print("별(*)로 풀어서 출력:", *re)
    
    print("\n" + "="*40 + "\n")

